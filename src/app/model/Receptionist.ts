export class Receptionist{
    receptionistName:string;
    receptionistPhone:number;
    receptionistEmail:string;
    receptionistPassword:string;
    receptionistId:number;
     constructor(receptionistName:string,receptionistPhone:number,receptionistEmail:string,receptionistPassword:string,receptionistId:number){
          this.receptionistName=receptionistName;
          this.receptionistPhone=receptionistPhone;
          this.receptionistEmail=receptionistEmail;
          this.receptionistPassword=receptionistPassword;
          this.receptionistId=receptionistId;
 
     }
 }